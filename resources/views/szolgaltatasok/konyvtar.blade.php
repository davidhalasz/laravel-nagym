        <p class="mt-6">
            A település központjában található művelődési ház a XX. század ötvenes éveinek közepén épült, az akkor
            szokásos tervek szerint: előtér, nagyterem+színpad, két klubszoba. Később épült hozzá a vizesblokk.<br />
            2009-ben, lakossági, polgári összefogással – több jótékonysági est szervezésével – részleges felújítására
            került sor. Megújult az előtér, a színpad és sor került a két klubszoba festésére is.<br />
            A művelődési ház fő feladata az anyagi lehetőségek figyelembevételével a különböző csoportok, együttesek
            működési feltételeinek – elsősorban a próbalehetőségek – biztosítása, az ismeretszerző, az amatőr alkotó,
            művelődő közösségek tevékenységének támogatása.<br />
            Szintén feladata a helyi művelődési szokások gondozása, illetve a szabadidő kulturális célú eltöltéséhez
            segítség adása. Lehetővé teszi, hogy a lakosság önszerveződő közösségei megfelelő rendszerességgel és
            időtartamban vehessék igénybe az intézmény szolgáltatásait.
        </p>
        <h4 class="text-xl mt-6 mb-2">
            Állandó programok rendezvények
        </h4>
        <p>
            Mivel a településen csak a művelődési házban található olyan nagyterem, melynek befogadóképessége 200 fő
            körüli, így minden rendezvény, mely ekkora helyet igényel, itt zajlik. Használja az iskola, önkormányzat,
            egyesületek, különböző civil szervezetek, az egyház és az amatőr művészeti csoportok. Itt kerülnek
            megrendezésre az ünnepi megemlékezések, a városi nagyrendezvények néhány programja, a hangversenyek és a
            kiállítások többsége is. De pl. a véradást és a különböző közgyűléseket is itt bonyolítják le.<br />
            Állandó programok a művészeti iskola néptánc tagozatának foglalkozásai, a Nagymarosi Városi Nyugdíjasklub
            összejövetelei, a Nagymarosi Férfikar, a Nagymarosi Női Kar, a Maroska Gyermekkórus, a Dunakanyar
            Fúvósegyüttes, a Wildenrosen Táncegyüttes és a Die Blumen Táncegyüttes próbái. Ezeken kívül a
            tánctanfolyamok és az angol tanfolyam foglalkozásai.<br />
            A rendezvények közül említést érdemelnek a komolyzenei hangversenyek, melyek közreműködői elsősorban a helyi
            zenei művészeti csoportok, illetve a komolyzenei tanulmányokat folytató helyi ifjú polgárok. Sikeresek a
            képzőművészeti/fotó kiállítások, évente átlag 4-5 alkalommal kerül sor tárlat rendezésére, köztük három
            mindig nagytermes. Színházi előadást gyermekek részére szervez a művelődési ház.<br />
            Esetenként családi rendezvények lebonyolítására is igénybe veheti a lakosság.<br />
            A művelődési ház, illetve munkatársai részt vesznek a városi nagyrendezvények szervezésében,
            lebonyolításában is.
        </p>

        <h3 class="text-2xl pt-6 pb-2 font-bold">Városi Könyvtár</h3>
        <p>
            Városunk könyvtára általános gyűjtőkörű közművelődési könyvtár.<br />
            Elsődleges feladata Nagymaros város lakosságának könyvtári ellátása.<br />
            Bárki – aki elfogadja használati szabályzatát – beiratkozhat és használhatja a gyűjteményt.
            A könyvtár a város központjában, a Váci út 11. sz. épületben működik. 2013-ban felújították,
            korszerűsítették, a bejárat közvetlenül az utcafrontra került. A könyvtár alapterülete nőtt, 270 m2.<br />
            Felnőtt kölcsönző részből (ellenőrzési pont), gyermekkönyvtári részből áll. A kézikönyvtári állomány a
            felnőtt kölcsönző térben – elkülönítve – kapott helyet. A tetőtéri szint elsősorban raktározási célt
            szolgál. Az intézményben szakképzett főfoglalkozású könyvtárosok dolgoznak.
        </p>
        <h4 class="text-xl mt-6 mb-2">
            A könyvtár gyűjteménye
        </h4>
        <p>
            A városi könyvtár állománya megközelítőleg 34 ezer dokumentum, mely elsősorban nyomtatott (könyv)
            dokumentumokból és minimális mennyiségű egyéb dokumentumból (hangzó- és képanyag) tevődik össze.<br />
            Köszönhetően a NKA támogatásának is, közel 70 féle folyóirat, heti- és napilap közül válogathatnak olvasók.
            Lehetőség szerint gyűjtjük a Nagymarosra vonatkozó irodalmat, mely a helytörténeti gyűjteményben kap helyet.
            Az állománygyarapításnál kiemelt figyelmet kapnak a 14 éven aluli olvasók (könyvtárunk az iskolai könyvtár
            szerepét is betölti), az éves gyarapítás 1/3-a gyermek- és ifjúsági mű.<br />
            Figyelmet fordítunk továbbá arra is, hogy – elsősorban az általános műveltséghez szükséges alapművek,
            lexikonok, enciklopédiák stb. – meglegyenek könyvtárunkban. Büszkék vagyunk gyűjteményünkre, hiszen az
            egyetemi, főiskolai tanulmányokat (elsősorban humán szakon) folytató olvasóinkat nagy százalékban tudjuk
            kiszolgálni, számukra szakirodalmat biztosítani.<br /><br />
