        <p>
            A védőnői hálózat megalakulása óta egyedülállóan képviseli a megelőzésnek azon formáját, mely Európa
            országain belül igazi hungarikumnak számít.<br />

            „Stefánia hercegasszony nevét viselő szövetség (1915) célul tűzte ki a csecsemőhalandóság csökkentését, a
            nemzet számbeli erősbítését, a tudatlanság és a babona elleni küzdelmet.”<br />
            A védőnők évtizedek óta fontos szerepet töltenek be a nő-, anya-, csecsemő- gyermek-, ifjúsági-és
            családvédelemben. Tevékenységének alapelve a megelőzés. Az egészségügy sajátos határterületén, önállóan, a
            különböző társszakmák képviselőivel együttműködve látják el a törvények és jogszabályok, módszertani
            útmutatók és a szakmai irányítás által meghatározott feladataikat.<br />
        </p>
        <p class="my-2">
            A védőnői szolgálat az alapellátás keretein belül működik, az ellátás ingyenes. Két részből áll: területi
            védőnői és iskolai védőnői ellátásból. Nagymaroson két védőnő dolgozik. Tevékenységüket egy földrajzilag
            meghatározott területen végzik a mindenkor érvényes védőnői utcajegyzék alapján. A területi védőnő nem
            választható. Munkánkat a védőnői tanácsadóban, az óvodákban, iskolában és a családok otthonában végezzük.
            Mivel Nagymaroson nincs iskolai státusz, ezért az iskolai feladatokat a területi védőnők látják el.
        </p>

