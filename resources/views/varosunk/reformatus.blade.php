        <h2 class="text-2xl font-bold mt-6 mb-4">
            A reformátusság története Nagymaroson
        </h2>
        <p>
            Egyházközségünk története egészen a reformáció koráig nyúlik vissza. A reformáció következtében az 1600-as
            évekre Nagymaroson nagy számban éltek reformátusok, jelentős gyülekezet alakult ki településünkön.<br />
            Azonban a pestisjárvány és a Rákóczi-szabadságharc következtében az 1700-as évek elejére mindössze 30
            református család élt Nagymaroson, a Magyar u – Fő tér – Molnár u. által határolt területen.<br />
            A bajorországi svábok Nagymarosra történő betelepítésének következményeképpen növekedni kezdett a katolikus
            vallásúak száma, a reformátusok száma pedig lecsökkent. A reformáció utáni hosszú évtizedekben a mai római
            katolikus Szent Kereszt Felmagasztalása templom szolgált a gyülekezet templomaként, később a 18. század
            végére felépült mai templomunk elődje 1798-ban a II. József nevével fémjelzett „türelmi rendeletnek”
            köszönhetően torony nélkül. A templom 1934-ben való átépítésekor kapta meg mai állapotát, Kós Károly tervei
            alapján.
        </p>
        <h2 class="text-2xl font-bold mt-6 mb-4">
            A Nagymarosi Református Egyházközség élete ma
        </h2>
        <p>
            Gyülekezetünk folyamatos növekedés mutat mind lelkiekben, mind a gyülekezet létszámát illetően. Az
            egyháztagok száma folyamatosan növekszik, melyek között vannak megtért keresők, beköltöző családok, felnőtt
            keresztségben és konfirmációban részesülők és helyi református keresztyén családban felnevelkedettek
            egyaránt. Küldetésünk, hogy az Úr Jézus Krisztusról szóló örömhírt tudjuk átadni városunkban. Szeretettel és
            nyitott szívvel fogadunk mindenkit, aki betér hozzánk.
