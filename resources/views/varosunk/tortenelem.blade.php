        <p>
            1324-ben Róbert Károly Budával azonos kiváltságokat biztosított Marosnak: szabad bíró- és lelkészválasztási
            jogot, önálló törvényhatóságot és vámmentességet. Mindezt 1345-ben Nagy Lajos, 1388-ban és 1436-ban Zsigmond
            megerősítette. 1464-ben Mátyás újabb jogokat adott, amelyeknek a körét 1492-ben II. Ulászló tovább
            bővítette. Ezeket a kiváltságokat Mohács után I. Ferdinánd, II. Mátyás, majd később I. Lipót megerősítették.
            A vidék egykori rangját tükrözi a hagyomány, amely szerint Róbert Károly ültette a máig is termő pompás
            szelídgesztenyést.Az 1396-ban kelt oklevelek bérbeadott fürdőről tesznek említést. 1587-ben kismezővárosként
            említették Marost.<br /><br />

            A reformáció erősen érintette a vidéket. A bányavárosok mintájára a kistelepülések is igyekeztek önálló
            esperességet alapítani. Ezeket nemzetiségenként hozták létre, egyetlen reformált hit szerint. A térségben
            két esperesség alakult. Maros a drégelypalánkihoz került. Az ellenreformáció 1726-ban visszaállította a
            honti római katolikus főesperességet, amelyhez Marost is csatolták.<br />
            A szatmári béke után a pálosok visszakapták ősi birtokaikat, köztük a nagymarosit is, mely a márianosztrai
            kolostorhoz tartozott. A várost 1644-ben I. Rákóczi György oltalomlevele védte a katonai beszállásolásoktól.

            1709-ben súlyos pestis pusztított a helységben. A 18. század második évtizedére rendkívüli mértékben
            megfogyatkozott a népesség. A török uralom, az ezzel járó harcok, garázdálkodások, majd a
            Rákóczi-szabadságharc idején átvonuló császári csapatok zaklatásai megtizedelték, elűzték a népet. A
            későbbiekben a pestisjárvány miatt pusztult a lakosság. Az ellenreformáció idején a rekatolizálni nem kívánó
            családok távozása is oka volt a népesség csökkenésének, ezt kihasználva Bécs 1713-1735 között számára
            megbízhatónak vélt németeket telepített a helységbe, főleg Frank földről (Mainzi választófejedelemség
            területe), Ausztriából és a sváb régiókból. A szorgalmas telepesek folytatták a vidéken a szőlő- és
            gyümölcstermesztést.<br /><br />

            A várost 1750 után Visegráddal együtt az óbudai koronauradalomhoz csatolták. 1772-ben nagy építkezések
            kezdődtek. Ekkor épült ki Nagymaros központja, középkori magja – a templom és környéke. Elkészült a fő utca,
            kezdetét vette a Duna-part beépítése, megjelentek a hegyek felé vezető első utak.<br /><br />

            A 20. század első felében nagyobb léptékű iparosítás bontakozott ki. A Danubiusz Papírgyár Rt. 1905-ben
            alakult, poháralátéteket és papírlemezeket gyártottak. A Petrás-féle kanálüzemben 1938-ban indult a
            termelés, alumínium kanalat gyártottak. A lenszalmagyár 1938-tól működött. A városi téglagyárban cserepet,
            téglát és kályhacsempét gyártottak. A gépgyár 1941-ben létesült. Ezek a kezdetben sikeres ipari cégek később
            megszűntek, illetve átalakultak.<br />
            1945 után több ipari vállalat alakult. Ekkor jött létre a szerszámkészítő üzem, a vas- és műszeripari
            szövetkezet, a galvanizáló üzem, a Duna-környéki ruházati és háziipari szövetkezet, valamint az erdei
            termékeket feldolgozó vállalat. A cégek közül néhány a vízierőmű építési munkálatainak kezdetén megszűnt,
            mások a térség rangos foglalkoztatóiként és exportőreiként napjainkban is sikeresen dolgoznak.<br /><br />

            Az 1900-as évek elejétől a modern ipar mellett az élénk kereskedelmi életnek is elismert helye volt
            Nagymaros. Az ipari termékek mellett a fő piaci áru a gyümölcs volt. A lakosság nagy része igen jó minőségű
            gyümölcs előállításával foglalkozott, amelynek egy része válogatás és osztályozás után, a nagykereskedők
            szervezésében a híres kofahajókon néhány óra alatt a bécsi piacokra került, de eljutott a marosi friss szőlő
            és málna a berlini, prágai fogyasztókhoz is. A gazdag gyümölcstermés másik részét a nyaralók körében
            értékesítették. A gyümölcstermesztés ma is országos hírű. A termelők a megelőző évtizedekben szakcsoportban
            tevékenykedtek. A kereskedelmi ellátás már 1945 előtt városi színvonalú volt.<br /><br />

            1946-1948-ban a német lakosság nagy részét kitelepítették, illetve “malenkij robot“-ra a Szovjetunióba
            hurcolták, s helyükbe 86 magyar család (520 fő) érkezett a Felvidékről.
