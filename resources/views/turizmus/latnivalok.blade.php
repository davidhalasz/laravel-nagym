        <h3>
            Nagymaros megközelítése
        </h3>
        <p>
            Aki Nagymarosra látogat, annak könnyű dolga lesz az élményszerzésben, hiszen már az utazás is izgalmas:
            jöhet akár sétahajóval is, amelynek a fedélzetéről páratlan látvány tárul a szeme elé. Ritkán csodálhatunk
            meg városokat egy hajóról, általában az így szerzett élmények még különlegesebbnek hatnak, ha a tervünk egy
            egynapos kirándulás Nagymaros szívében.<br />

            Vonattal szintén hamar, nagyjából 50 perc alatt már ki is érhetünk Nagymarosra Budapestről, de kocsival sem
            tart egy óránál tovább, és a főtéren kényelmesen le is lehet parkolni. (A parkoló a hét minden napján
            fizetős.)<br />

            Érkezhetünk akár biciklivel is, hiszen Gödtől egybefüggő kerékpárút van kiépítve egészen Szobig, így bárhol
            becsatlakozhatunk: például Vácig eljövünk autóval, és onnan tekerünk ki Marosra. A bicikliút végig
            biztonságos, egyedül a Halasnál keresztezi az autóutat, mivel a főútról itt kanyarodnak le a komphoz.
        </p>

        <h3>
            Komppal Nagymarosra
        </h3>
        <p>
            A komp számos plusz lehetőséget kínál a kirándulók számára.<br />

            Ha a túloldalról érkeznénk Nagymarosra, akkor a legokosabb az autót ott hagyni, és gyalogosan (vagy
            biciklivel) átkelni a kompon. Ez természetesen fordítva is igaz, aki a túloldalt szeretné kicsit bejárni és
            felfedezni, annak a legegyszerűbb, ha a főtéri parkolóban hagyja az autóját, és úgy megy a komphoz. A komp
            óránként jár, minden óra 45-kor indul Visegrádról, és egészkor fordul vissza Nagymarosról.<br />

            A nyári kiemelt időszakokban sokszor megesik (főleg a Palotajátékok ideje alatt, július második hétvégéjén),
            hogy többször fordul a komp egy órán belül is. Ez az utasok számától függ, és sosem lehet előre tudni, a
            napi forgalom függvénye.<br />

            Minden járműre szükséges külön jegyet venni, így biciklire is. A személyjegyek esetében van lehetőség diák-
            és nyugdíjas jegy vásárlására is.<br />

            Ha autóval érkeznénk, körülbelül egy óra alatt már ide is érünk Budapesttől – 64 km. ha pedig a másik
            irányból, például Esztergomból, az is csupán 45 percet vesz igénybe – 40 km. A leggyorsabb, ha Esztergomból
            Szlovákián keresztül jövünk egészen Marosig. Minden ki van táblázva, zavartalanul utazhatunk. A visegrádi
            oldalról is jöhetünk, ez valamivel hosszabb utat jelent, illetve alkalmazkodni kell a komp menetrendjéhez –
            hiszen óránként jár csak. Illetve a nyári –kiemelt- időszakban tele van a komp, így ha ezt a zsúfoltságot
            elkerülnénk, javasoljuk az egyenes utat Esztergomból Nagymarosig a Duna bal partján.<br />

            Összességében elmondhatjuk, hogy egy egynapos kirándulás Nagymaros szívében felejthetetlen élmény, de
            valószínűleg egy nap kevésnek is fog bizonyulni. Aki egyszer ide látogat, az vissza is akar majd térni. A
            tájért. A hangulatért. A vidéki bájáért. A hekkért. A piacért. De legfőképpen azért, mert Nagymaros a maga
            természetességével különleges.<br />

            Szerényen, de határozottan vonja hatása alá az ide látogatókat. Nem érdemes kihagyni, ezt át kell élni!
        </p>
